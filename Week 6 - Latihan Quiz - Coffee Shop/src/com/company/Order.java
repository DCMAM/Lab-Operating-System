package com.company;

public class Order {
    String name;
    int rating, price, quantity;

    // ctrl + shift + s -> constructor & getter setter
    public Order(String name, int rating, int price, int quantity) {
        this.name = name;
        this.rating = rating;
        this.price = price;
        this.quantity = quantity;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
